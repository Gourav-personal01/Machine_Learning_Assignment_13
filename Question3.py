# # Q3. How do you interpret the coefficients of a Lasso Regression model?

# # Answer - 

# Like OLS regression, the sign (positive or negative) of the coefficients in Lasso Regression indicates the direction of the relationship between a predictor and the target variable. A positive coefficient suggests a positive relationship, while a negative coefficient suggests a negative relationship.

# Lasso Regression applies an L1 regularization penalty that shrinks the magnitude of some coefficients toward zero. 

# A key interpretation aspect of Lasso Regression is feature selection. If a coefficient is not set to zero, it implies that the corresponding predictor is considered important by the mode