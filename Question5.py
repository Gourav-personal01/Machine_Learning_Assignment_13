# Q5. Can Lasso Regression be used for non-linear regression problems? If yes, how?

# Answer - 


# Lasso Regression, in its standard form, is primarily designed for linear regression problems, where the relationship between predictors 
# and the target variable is assumed to be linear. However, it can be extended to handle non-linear 
# regression problems through a process called "feature engineering" or by incorporating non-linear transformations of the predictors. 